    MANG CHAR(4) PRIMARY KEY NOT NULL,
    TENNG VARCHAR(100)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

CREATE TABLE CANBO (
    MACB VARCHAR(16) PRIMARY KEY NOT NULL,
    TENCB VARCHAR(100),
    SDTCB INT,
    DONVI VARCHAR(50),
    MANG CHAR(4),
    FOREIGN KEY (MANG) REFERENCES NGANH(MANG)
