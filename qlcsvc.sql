CREATE TABLE PHONG_XUONG (
    MAPX CHAR(10) PRIMARY KEY NOT NULL,
    TENPX VARCHAR(150),
    MANG CHAR(4),
    MACB VARCHAR(16),
    GiaiDoanQL VARCHAR(100),
    SoTuan INT,
    GioQuyDoi INT,
    FOREIGN KEY (MANG) REFERENCES NGANH(MANG),
    FOREIGN KEY (MACB) REFERENCES CANBO(MACB)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

CREATE TABLE TAISANPHONG_XUONG (
    MADO INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
    TENDO VARCHAR(150),
    THONGSOKT VARCHAR(1000),
    MAPX CHAR(10),
    NOISX VARCHAR(16),
    DVT VARCHAR(8),
    SLGSS INT,
    SLGTT INT,
    SLTHUA INT,
    SLTHIEU INT,
    SLSD INT,
    GIAITRINH VARCHAR(150),
    GhiChu VARCHAR(50),
    FOREIGN KEY (MAPX) REFERENCES PHONG_XUONG(MAPX)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;

